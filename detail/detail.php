        // カレンダーのデータを表示
        window.onload = function () {
            showCalendarData();
            addEventListeners();
        };

        function showCalendarData() {
            for (var day in calendarData) {
                var events = calendarData[day];
                for (var time in events) {
                    var cellId = day.toLowerCase() + "-" + time;
                    var cell = document.getElementById(cellId);
                    if (cell) {
                        cell.textContent = events[time];
                    }
                }
            }
        }

        function addEventListeners() {
            var cells = document.getElementsByTagName("td");
            for (var i = 0; i < cells.length; i++) {
                cells[i].addEventListener("click", function () {
                    editSchedule(this);
                });
            }
        }

        function editSchedule(cell) {
            var cellId = cell.getAttribute("id");
            var day = cellId.split("-")[0];
            var time = cellId.split("-")[1];
            var schedule = prompt("タスクを入力してください:");
            if (schedule !== null) {
                calendarData[day][time] = schedule;
                cell.textContent = schedule;
            }
        }
